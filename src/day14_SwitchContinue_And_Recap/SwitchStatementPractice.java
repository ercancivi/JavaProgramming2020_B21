package day14_SwitchContinue_And_Recap;

public class SwitchStatementPractice {

    public static void main(String[] args) {

        char a = 'A';

        switch (a){

            case 'a':
                System.out.println("One");
                break;

            case 'A':
                System.out.println("Two");
                break;

        }

    }

}
