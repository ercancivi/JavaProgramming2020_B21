package day20_ForLoop.warmUpTasks_ForLoop;

public class PrintingAlphabetLetters {

    public static void main(String[] args) {

        /*  WarmUp Task 1:
            Write a program that will print out all alphabet letters
         */
        for(char i = 'A'; i <= 'Z'; i++) {
            System.out.print(i + " ");
        }

    }

}
