package day03_Variables;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello Everyone");
        System.out.println("My name is Ercan, I am from Richmond, TX");

        System.out.println("========================================");

        //EscapeSequences ==> adding new line ==> \n
        System.out.println("Hello Everyone\nMy name is Ercan, I am from Richmond, TX");

        System.out.println("========================================");

        //EscapeSequences ==> adding tab ==> \t
        System.out.println("\tToday I learned Java");
        System.out.println("it was so much fun");

        System.out.println("========================================");

        //EscapeSequences ==> adding backslash \ ==> \\
        System.out.println("/");    //prints single front slash
        System.out.println("\\");   //prints single backslash

        System.out.println("========================================");

        //EscapeSequences ==> adding single quote ' ==> \'
        System.out.println("My name is 'Ercan'");
        System.out.println("My name is \'Ercan\'");

        System.out.println("========================================");

        //EscapeSequences ==> adding double quote " ==> \"
        System.out.println("My name is Ercan");
        System.out.println("My name is \"Ercan\"");
        System.out.println("My favorite TV Show is \"Game of Thrones\"");



    }

}
