package day04_Variables;

public class Variables_Practice {

    public static void main(String[] args) {
        /* Task01:
            create a class called variables_Practice:
                declare the following variables:
                    num1 = 100;
                    num2 = 10000;
                    num3 = 3.5;
                    num4 = 2.5f;
                    num5 = 999999999999;
                    num6 = 850000;
                print each of the variables above on the console
         */
        byte num1 = 100;
        System.out.print("num1 is = ");
        System.out.println(num1);

        short num2 = 10000;
        System.out.print("num2 is = ");
        System.out.println(num2);

        double num3 = 3.5;
        System.out.print("num3 is = ");
        System.out.println(num3);

        float num4 = 2.5f;
        System.out.print("num4 is = ");
        System.out.println(num4);

        long num5 = 999999999999L;
        System.out.print("num5 is = ");
        System.out.println(num5);

        int num6 = 850000;
        System.out.print("num6 is = ");
        System.out.println(num6);

    }

}
