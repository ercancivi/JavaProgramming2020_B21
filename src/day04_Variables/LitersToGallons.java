package day04_Variables;

public class LitersToGallons {

    public static void main(String[] args) {

        /* Tasks03:
            create a class called LitersToGallons,
                and write a program that can convert the given liters to gallons and print out the result

         */

        double liter1 = 16.0;
        double gallon1 = 0.264172 * liter1;
        System.out.print("output1 is = ");
        System.out.println(gallon1);

        double liter2 = 25.0;
        double gallon2 = 0.264172 * liter2;
        System.out.print("output2 is = ");
        System.out.println(gallon2);

    }

}
