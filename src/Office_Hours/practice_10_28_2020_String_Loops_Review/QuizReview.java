package Office_Hours.practice_10_28_2020_String_Loops_Review;

public class QuizReview {

    public static void main(String[] args) {
        int x = 10; // x = 11+1 = 12;
        int y = x++; // y = 10+1 = 11;

        System.out.println( y++ + " " +  x++  + " " + y);  // 10  11  11
        //                  10 + " " +   11 +"  " + 11

    }

}
